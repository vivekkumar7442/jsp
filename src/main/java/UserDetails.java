
public class UserDetails {
	
	private Integer userId;
	
	private String name;
	
	private String address;
	
	private String email;
	
	private String mobile;
	
	
	private String officeMobile;
	
	private String mobileHome;

	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getOfficeMobile() {
		return officeMobile;
	}

	public void setOfficeMobile(String officeMobile) {
		this.officeMobile = officeMobile;
	}

	public String getMobileHome() {
		return mobileHome;
	}

	public void setMobileHome(String mobileHome) {
		this.mobileHome = mobileHome;
	}
	
	
	

}
